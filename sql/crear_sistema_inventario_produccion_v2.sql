create table if not exists public.insumos (
  id uuid primary key default gen_random_uuid(),
  nombre text not null,
  categoria text not null default 'general',
  unidad text not null default 'unidad',
  stock_actual numeric(14,4) not null default 0,
  stock_minimo numeric(14,4) not null default 0,
  costo_unitario numeric(14,4) not null default 0,
  proveedor text,
  activo boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.stock_productos (
  id uuid primary key default gen_random_uuid(),
  producto_id uuid not null references public.productos(id) on delete cascade,
  stock_actual numeric(14,4) not null default 0,
  stock_minimo numeric(14,4) not null default 0,
  costo_promedio numeric(14,4) not null default 0,
  activo boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint stock_productos_producto_id_key unique (producto_id)
);

create table if not exists public.inventario_config_productos (
  id uuid primary key default gen_random_uuid(),
  producto_id uuid not null references public.productos(id) on delete cascade,
  controla_inventario boolean not null default true,
  modo_consumo text not null default 'sin_control',
  permite_stock_negativo boolean not null default false,
  notas text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint inventario_config_producto_key unique (producto_id),
  constraint inventario_config_modo_check check (modo_consumo in ('receta', 'stock_producto', 'sin_control'))
);

create table if not exists public.recetas (
  id uuid primary key default gen_random_uuid(),
  producto_id uuid not null references public.productos(id) on delete cascade,
  nombre text not null,
  descripcion text,
  rendimiento numeric(14,4) not null default 1,
  activo boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint recetas_producto_key unique (producto_id),
  constraint recetas_rendimiento_check check (rendimiento > 0)
);

create table if not exists public.recetas_detalle (
  id uuid primary key default gen_random_uuid(),
  receta_id uuid not null references public.recetas(id) on delete cascade,
  insumo_id uuid not null references public.insumos(id) on delete restrict,
  cantidad numeric(14,4) not null,
  unidad text not null default 'unidad',
  created_at timestamptz not null default now(),
  constraint recetas_detalle_cantidad_check check (cantidad > 0)
);

create table if not exists public.ordenes_produccion (
  id uuid primary key default gen_random_uuid(),
  numero_orden bigint generated by default as identity,
  producto_id uuid references public.productos(id) on delete set null,
  fecha date not null default current_date,
  estado text not null default 'completada',
  cantidad_producida numeric(14,4) not null default 0,
  notas text,
  cajero text,
  cajero_id text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint ordenes_produccion_estado_check check (estado in ('borrador', 'en_proceso', 'completada', 'cancelada'))
);

create table if not exists public.ordenes_produccion_detalle (
  id uuid primary key default gen_random_uuid(),
  orden_id uuid not null references public.ordenes_produccion(id) on delete cascade,
  tipo text not null,
  producto_id uuid references public.productos(id) on delete set null,
  insumo_id uuid references public.insumos(id) on delete set null,
  cantidad numeric(14,4) not null,
  costo_unitario numeric(14,4) not null default 0,
  created_at timestamptz not null default now(),
  constraint ordenes_produccion_detalle_tipo_check check (tipo in ('consumo_insumo', 'producto_terminado')),
  constraint ordenes_produccion_detalle_cantidad_check check (cantidad > 0)
);

create table if not exists public.movimientos_inventario (
  id uuid primary key default gen_random_uuid(),
  item_tipo text not null,
  tipo text not null,
  referencia_tipo text,
  referencia_id text,
  insumo_id uuid references public.insumos(id) on delete set null,
  producto_id uuid references public.productos(id) on delete set null,
  cantidad numeric(14,4) not null,
  saldo_resultante numeric(14,4),
  costo_unitario numeric(14,4) not null default 0,
  nota text,
  cajero text,
  cajero_id text,
  created_at timestamptz not null default now(),
  constraint movimientos_item_tipo_check check (item_tipo in ('insumo', 'producto')),
  constraint movimientos_tipo_check check (
    tipo in (
      'entrada',
      'salida',
      'ajuste_positivo',
      'ajuste_negativo',
      'venta',
      'produccion_entrada',
      'produccion_salida'
    )
  )
);

comment on table public.insumos is 'Materias primas, empaques e ingredientes.';
comment on table public.stock_productos is 'Stock de productos terminados listos para vender.';
comment on table public.inventario_config_productos is 'Define cómo afecta una venta al inventario por producto.';
comment on table public.recetas is 'Receta maestra por producto de venta.';
comment on table public.recetas_detalle is 'Insumos requeridos por receta.';
comment on table public.ordenes_produccion is 'Lotes/producciones terminadas.';
comment on table public.movimientos_inventario is 'Kardex auditado de entradas, salidas, ventas y producción.';

