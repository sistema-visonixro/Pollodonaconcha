create or replace view public.v_inventario_resumen as
select
  i.id,
  'insumo'::text as tipo_item,
  i.nombre,
  i.categoria,
  i.unidad,
  i.stock_actual,
  i.stock_minimo,
  i.costo_unitario,
  round(i.stock_actual * i.costo_unitario, 4) as valor_total,
  (i.stock_actual <= i.stock_minimo) as alerta_stock_bajo
from public.insumos i
where i.activo = true

union all

select
  sp.id,
  'producto'::text as tipo_item,
  p.nombre,
  p.tipo as categoria,
  'unidad'::text as unidad,
  sp.stock_actual,
  sp.stock_minimo,
  sp.costo_promedio as costo_unitario,
  round(sp.stock_actual * sp.costo_promedio, 4) as valor_total,
  (sp.stock_actual <= sp.stock_minimo) as alerta_stock_bajo
from public.stock_productos sp
join public.productos p on p.id = sp.producto_id
where sp.activo = true;

create or replace view public.v_recetas_resumen as
select
  r.id,
  r.producto_id,
  p.nombre as producto,
  p.tipo as categoria_producto,
  r.rendimiento,
  count(rd.id) as cantidad_insumos,
  r.activo,
  r.updated_at
from public.recetas r
join public.productos p on p.id = r.producto_id
left join public.recetas_detalle rd on rd.receta_id = r.id
group by r.id, r.producto_id, p.nombre, p.tipo, r.rendimiento, r.activo, r.updated_at;

-- ============================================================================
-- 8) RLS ABIERTA (SIGUIENDO EL PATRÓN ACTUAL DEL PROYECTO)
-- ============================================================================

alter table public.insumos enable row level security;
alter table public.stock_productos enable row level security;
alter table public.inventario_config_productos enable row level security;
alter table public.recetas enable row level security;
alter table public.recetas_detalle enable row level security;
alter table public.ordenes_produccion enable row level security;
alter table public.ordenes_produccion_detalle enable row level security;
alter table public.movimientos_inventario enable row level security;

do $$
declare
  t text;
  tables text[] := array[
    'insumos',
    'stock_productos',
    'inventario_config_productos',
    'recetas',
    'recetas_detalle',
    'ordenes_produccion',
    'ordenes_produccion_detalle',
    'movimientos_inventario'
  ];
begin
  foreach t in array tables
  loop
    if not exists (
      select 1
      from pg_policies
      where schemaname = 'public'
        and tablename = t
        and policyname = 'full_access_' || t
    ) then
      execute format(
        'create policy %I on public.%I for all to public using (true) with check (true)',
        'full_access_' || t,
        t
      );
    end if;
  end loop;
end $$;

